package com.practice;

public class NewStatementMethod {
	
	
	
	public static void main(String[] args) {

		// for loop

		for (int i = 10; i >= 1; i--) {
			System.out.println(i);
		}
		for (int a = 1; a <= 100; a++) {
			System.out.println(a);
		}
		for (int b = 100; b >= 1; b--) {
			System.out.println(b);
		}
		for (int c = 1; c <= 20; c += 3) {
			System.out.println(c);
		}
	}
}
